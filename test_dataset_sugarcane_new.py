def median_of(grade, key):
    vals = [f[key] for f in reference_features[grade]]
    return float(np.median(vals)) if vals else 0.0


dark_g3, dark_g4 = median_of(3, 'dark_patch_ratio'), median_of(4, 'dark_patch_ratio')
DARK_THRESHOLD = (dark_g3 + dark_g4) / 2 if dark_g4 > dark_g3 else max(dark_g3, dark_g4) * 0.7 + 0.05

white_g2, white_g3 = median_of(2, 'white_patch_ratio'), median_of(3, 'white_patch_ratio')
WHITE_THRESHOLD = (white_g2 + white_g3) / 2 if white_g3 > white_g2 else max(white_g2, white_g3) * 0.7 + 0.05

def advanced_spread_score(f):
    return f['aspect_ratio'] + (2.0 * f['num_components']) + (f['major_axis_px'] * 0.01)

spread_g1 = float(np.median([advanced_spread_score(f) for f in reference_features[1]])) if reference_features[1] else 0.0
spread_g2 = float(np.median([advanced_spread_score(f) for f in reference_features[2]])) if reference_features[2] else 0.0
SPREAD_THRESHOLD = (spread_g1 + spread_g2) / 2

print("Calibrating Threasholds:")
print(f"  dark_patch_ratio:  Grade3 median={dark_g3:.3f}  Grade4 median={dark_g4:.3f}  -> threshold={DARK_THRESHOLD:.3f}")
print(f"  white_patch_ratio: Grade2 median={white_g2:.3f}  Grade3 median={white_g3:.3f}  -> threshold={WHITE_THRESHOLD:.3f}")
print(f"  advanced_spread:   Grade1 median={spread_g1:.3f}  Grade2 median={spread_g2:.3f}  -> threshold={SPREAD_THRESHOLD:.3f}")


#Deployment
def classify(feats):
    dark_margin = feats['dark_patch_ratio'] - DARK_THRESHOLD
    if dark_margin > 0:
        return 4

    white_margin = feats['white_patch_ratio'] - WHITE_THRESHOLD
    if white_margin > 0:
        return 3


    s = advanced_spread_score(feats) - SPREAD_THRESHOLD
    return 2 if s > 0 else 1


