def get_foreground_mask(img_bgr):
    h, w = img_bgr.shape[:2]
    diag = np.sqrt(h * h + w * w)
    hsv = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2HSV)
    H, S, V = hsv[:, :, 0], hsv[:, :, 1], hsv[:, :, 2]

    green_strict = (H >= 28) & (H <= 95) & (S > 40) & (V > 30)
    g_u8 = cv2.morphologyEx((green_strict.astype(np.uint8)) * 255, cv2.MORPH_CLOSE, np.ones((9, 9), np.uint8))
    g_u8 = cv2.morphologyEx(g_u8, cv2.MORPH_OPEN, np.ones((5, 5), np.uint8))
    n, labels, stats, _ = cv2.connectedComponentsWithStats(g_u8, connectivity=8)

    use_fallback = True
    fg_u8 = None
    if n > 1:
        green_area = stats[1:, cv2.CC_STAT_AREA].max()
        if green_area > 0.06 * (h * w):
            largest = int(np.argmax(stats[1:, cv2.CC_STAT_AREA])) + 1
            leaf_core = labels == largest
            dil = max(15, int(0.045 * diag))
            if dil % 2 == 0:
                dil += 1
            kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (dil, dil))
            claimed = cv2.dilate(leaf_core.astype(np.uint8), kernel) > 0
            _, satc = cv2.threshold(S, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
            satc = satc.astype(bool)
            fg = leaf_core | (claimed & satc) | (claimed & (V > 60))
            fg_u8 = cv2.morphologyEx((fg.astype(np.uint8)) * 255, cv2.MORPH_CLOSE, np.ones((7, 7), np.uint8))
            use_fallback = False

    if use_fallback:
        _, sat_candidate = cv2.threshold(S, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
        fg_u8 = cv2.morphologyEx(sat_candidate, cv2.MORPH_CLOSE, np.ones((9, 9), np.uint8))
        fg_u8 = cv2.morphologyEx(fg_u8, cv2.MORPH_OPEN, np.ones((7, 7), np.uint8))

    n2, labels2, stats2, _ = cv2.connectedComponentsWithStats(fg_u8, connectivity=8)
    if n2 > 1:
        largest2 = int(np.argmax(stats2[1:, cv2.CC_STAT_AREA])) + 1
        fg_u8 = (labels2 == largest2).astype(np.uint8) * 255
    return fg_u8.astype(bool)


def get_midrib_zone(img_bgr, fg_mask):
    lab = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2LAB); L = lab[:, :, 0]
    hsv = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2HSV); S = hsv[:, :, 1]
    fgL, fgS = L[fg_mask], S[fg_mask]
    if fgL.size < 30:
        return fg_mask
    L_thresh = np.percentile(fgL, 55)
    S_thresh = np.percentile(fgS, 45)
    pale = (L > L_thresh) & (S < S_thresh) & fg_mask
    pale_u8 = cv2.morphologyEx((pale.astype(np.uint8)) * 255, cv2.MORPH_CLOSE, np.ones((17, 17), np.uint8))
    n, labels, stats, _ = cv2.connectedComponentsWithStats(pale_u8, connectivity=8)
    if n <= 1:
        return fg_mask
    largest = int(np.argmax(stats[1:, cv2.CC_STAT_AREA])) + 1
    midrib_core = labels == largest
    diag = np.sqrt(img_bgr.shape[0] ** 2 + img_bgr.shape[1] ** 2)
    dil = max(15, int(0.05 * diag))
    if dil % 2 == 0:
        dil += 1
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (dil, dil))
    zone = cv2.dilate(midrib_core.astype(np.uint8), kernel) > 0
    zone = zone & fg_mask
    return zone if zone.sum() > 0.05 * fg_mask.sum() else fg_mask


def get_glare_mask(img_bgr):
    hsv = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2HSV)
    v, s = hsv[:, :, 2], hsv[:, :, 1]
    glare = (v > 245) & (s < 18)
    return cv2.dilate(glare.astype(np.uint8), np.ones((3, 3), np.uint8)).astype(bool)


def get_lesion_color_mask(img_bgr, search_zone, glare_mask, fg_fallback=None):
    lab = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2LAB); L, A = lab[:, :, 0], lab[:, :, 1]
    hsv = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2HSV); Sat = hsv[:, :, 1]

    def build_red(zone, lenient=False):
        zv = A[zone].astype(np.float32)
        if zv.size < 10:
            return np.zeros_like(zone)
        a_for_otsu = A.copy(); a_for_otsu[~zone] = int(zv.mean())
        otsu_t, _ = cv2.threshold(a_for_otsu, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
        mult = 1.0 if lenient else 2.0
        stat_t = zv.mean() + mult * zv.std()
        red_t = min(otsu_t, stat_t)
        sat_floor = 28.0 if lenient else 38.0
        return (A.astype(np.float32) > red_t) & (Sat > sat_floor) & zone

    red_candidate = build_red(search_zone)
    if red_candidate.sum() < 25 and fg_fallback is not None and fg_fallback.sum() > search_zone.sum():
        red_candidate = build_red(fg_fallback, lenient=True)
        search_zone = fg_fallback

    zL = L[search_zone]
    dark_t = np.percentile(zL, 35) if zL.size >= 10 else 90
    necrotic_raw = (L < dark_t) & (Sat < 70) & search_zone
    if red_candidate.any():
        vicinity = cv2.dilate(red_candidate.astype(np.uint8),
                               cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (31, 31))) > 0
        necrotic_candidate = necrotic_raw & vicinity
    else:
        necrotic_candidate = np.zeros_like(necrotic_raw)

    lesion = (red_candidate | necrotic_candidate) & (~glare_mask)
    lesion_u8 = cv2.morphologyEx((lesion.astype(np.uint8)) * 255, cv2.MORPH_OPEN, np.ones((3, 3), np.uint8))
    lesion_u8 = cv2.morphologyEx(lesion_u8, cv2.MORPH_CLOSE, np.ones((5, 5), np.uint8))

    n, labels, stats, _ = cv2.connectedComponentsWithStats(lesion_u8, connectivity=8)
    clean = np.zeros_like(lesion_u8)
    img_diag = np.sqrt(img_bgr.shape[0] ** 2 + img_bgr.shape[1] ** 2)
    if n > 1:
        areas = stats[1:, cv2.CC_STAT_AREA]
        keep_ids = np.argsort(areas)[::-1][:6] + 1
        min_area = max(5, 0.00015 * img_bgr.shape[0] * img_bgr.shape[1])
        for cid in keep_ids:
            area = stats[cid, cv2.CC_STAT_AREA]
            if area < min_area:
                continue
            bw, bh = stats[cid, cv2.CC_STAT_WIDTH], stats[cid, cv2.CC_STAT_HEIGHT]
            bbox_diag = np.sqrt(bw ** 2 + bh ** 2)
            solidity = area / max(1, bw * bh)
            if bbox_diag > 0.15 * img_diag and solidity < 0.35:
                continue
            clean[labels == cid] = 255
    return clean.astype(bool)


#Feature Engineering
def get_lesion_silhouette(lesion_color_mask, gap_px=13):
    if lesion_color_mask.sum() < 10:
        return lesion_color_mask
    k = gap_px if gap_px % 2 == 1 else gap_px + 1
    kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (k, k))
    bridged = cv2.dilate(lesion_color_mask.astype(np.uint8), kernel) > 0
    bridged_u8 = (bridged.astype(np.uint8)) * 255
    n, labels, stats, _ = cv2.connectedComponentsWithStats(bridged_u8, connectivity=8)
    if n <= 1:
        return lesion_color_mask
    largest = int(np.argmax(stats[1:, cv2.CC_STAT_AREA])) + 1
    return labels == largest


def compute_patch_ratios(L, S, lesion_color_mask, silhouette):
    interior = silhouette
    if silhouette.sum() > 60:
        eroded = cv2.erode(silhouette.astype(np.uint8), np.ones((3, 3), np.uint8)) > 0
        if eroded.sum() > 20:
            interior = eroded

    red_part = lesion_color_mask & interior
    if red_part.sum() < 5:
        red_part = lesion_color_mask if lesion_color_mask.sum() >= 5 else interior
    red_L_med = float(np.median(L[red_part]))
    red_S_med = float(np.median(S[red_part]))
    white_patch = interior & (L > red_L_med + 15) & (S < red_S_med - 15)
    dark_patch = interior & (L < red_L_med - 15)
    sil_px = max(1, int(interior.sum()))
    return float(white_patch.sum()) / sil_px, float(dark_patch.sum()) / sil_px


def compute_overall_darkness(L, lesion_color_mask, fg_mask):
    if lesion_color_mask.sum() < 5:
        return 0.0
    healthy = fg_mask & (~lesion_color_mask)
    if healthy.sum() < 20:
        return 0.0
    healthy_L = float(np.percentile(L[healthy], 70))
    lesion_L_med = float(np.median(L[lesion_color_mask]))
    if healthy_L < 1:
        return 0.0
    return float(np.clip(1.0 - lesion_L_med / healthy_L, 0.0, 1.0))


def get_shape_features(silhouette):
    lu = (silhouette.astype(np.uint8)) * 255
    n, labels, stats, _ = cv2.connectedComponentsWithStats(lu, connectivity=8)
    comps = [(cid, stats[cid, cv2.CC_STAT_AREA]) for cid in range(1, n) if stats[cid, cv2.CC_STAT_AREA] >= 4]
    num_components = len(comps)
    if num_components == 0:
        return 1.0, 0, 0.0
    comps.sort(key=lambda x: -x[1])
    largest_id = comps[0][0]
    comp_mask = (labels == largest_id).astype(np.uint8) * 255
    contours, _ = cv2.findContours(comp_mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    if not contours:
        return 1.0, num_components, 0.0
    (w, h) = cv2.minAreaRect(contours[0])[1]
    aspect = max(w, h) / min(w, h) if min(w, h) > 1e-3 else 1.0
    major_axis = max(w, h)
    return float(aspect), num_components, float(major_axis)


def extract_features(img_bgr):
    fg = get_foreground_mask(img_bgr)
    zone = get_midrib_zone(img_bgr, fg)
    glare = get_glare_mask(img_bgr)
    lesion = get_lesion_color_mask(img_bgr, zone, glare, fg_fallback=fg)

    lesion_px = int(lesion.sum())
    if lesion_px < 8:
        return dict(white_patch_ratio=0.0, dark_patch_ratio=0.0, aspect_ratio=1.0,
                     num_components=0, major_axis_px=0.0, lesion_pixels=0), lesion

    lab = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2LAB); L = lab[:, :, 0].astype(np.float32)
    hsv = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2HSV); S = hsv[:, :, 1].astype(np.float32)

    silhouette = get_lesion_silhouette(lesion)
    white_patch_ratio, dark_patch_ratio_internal = compute_patch_ratios(L, S, lesion, silhouette)
    overall_darkness = compute_overall_darkness(L, lesion, fg)

    dark_patch_ratio = max(dark_patch_ratio_internal, overall_darkness)
    aspect_ratio, num_components, major_axis_px = get_shape_features(silhouette)

    feats = dict(white_patch_ratio=white_patch_ratio, dark_patch_ratio=dark_patch_ratio,
                 aspect_ratio=aspect_ratio, num_components=num_components,
                 major_axis_px=major_axis_px, lesion_pixels=lesion_px)
    return feats, lesion


